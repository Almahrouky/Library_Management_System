    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String info = staffInfo.getText();
        try {
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/library_management_system",
                "root",
                "MHRQ"
            );
            
            // try one with book id and one with book name
            
            String query1 = """
                            select * from staff
                            where id = ?;
                            """;
            PreparedStatement statement = connection.prepareStatement(query1);
            statement.setString(1, info); // index (1-based), value
            
            ResultSet result1 = statement.executeQuery();
            
            if(result1.next()){
                
                query1 = """
                        delete from staff
                        where id = ?;
                        """;
                statement = connection.prepareStatement(query1);
                statement.setString(1, info); // index (1-based), value

                int result2 = statement.executeUpdate();
                
            }else{
                String query = """
                            select * from staff
                            where staff_name = ?;
                            """;
                statement = connection.prepareStatement(query);
                statement.setString(1, info); // index (1-based), value

                ResultSet result = statement.executeQuery();
                if(result.next()){
                    query1 = """
                        delete from staff
                        where staff_name = ?;
                        """;
                    statement = connection.prepareStatement(query1);
                    statement.setString(1, info); // index (1-based), value

                    int result2 = statement.executeUpdate();
                }else{
                    JOptionPane.showMessageDialog(this, "This staff not found");
                }
                result.close();
            }
            
            result1.close();
            statement.close();
            connection.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

